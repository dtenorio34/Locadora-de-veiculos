    private void exibeInformacoes(){
         ArrayList<Carro> x = ft.read();
       ft.frotaInicial();
        // lipar tabela
        for (int i = 0; i < 100; i++) {
            TabelaCadastro.setValueAt("", i, 0);
            TabelaCadastro.setValueAt("", i, 1);
        }
        
        for (int i = 0; i < x.size() && x.get(i)!=null ; i++) {
           TabelaCadastro.setValueAt(x.get(i).getModelo() , i, 0);
           TabelaCadastro.setValueAt(x.get(i).getCor(),i, 1);
        }
    }
